/**
 * だし紐づけ（2026-07-23オーナー確定）。
 * 材料「だし汁」（だし・和風だし等の表記揺れを含む）の行から、収録レシピ「だしのとり方」の
 * 詳細へ飛べるリンクを出すための判定ロジック（純ロジック・DB非依存）。
 * だしレシピ本来の目的（材料のだし汁をどう用意するか）へ導くための導線。
 */

/** 収録レシピ「だしのとり方」の料理名（この名前のレシピが端末にあるときだけリンクを出す） */
export const DASHI_RECIPE_TITLE = 'だしのとり方'

/**
 * 「だし汁」とみなす材料名の集合（素直な表記揺れのみ）。
 * だしの素（顆粒だし）は別物（だし汁ではなく調味料）なので意図的に含めない。
 */
const DASHI_INGREDIENT_NAMES = new Set([
  'だし汁',
  'だし',
  '出汁',
  'だし汁（和風だし）',
  '和風だし',
  '和風だし汁',
  'かつおだし',
  'かつおだし汁',
  'こんぶだし',
  '昆布だし',
  'いりこだし',
  '煮干しだし',
  'あわせだし',
  '合わせだし',
])

/**
 * 材料名が「だし汁」系か判定する。
 * 用途を示す丸括弧の補足（例:「だし汁(つゆ用)」「だし汁（卵液用）」）は取り除いてから照合する。
 * 「水またはだし汁」のような複合表記や「だしの素」は対象外（素直な一致のみを拾う保守的な設計）。
 */
export function isDashiIngredientName(name: string): boolean {
  const base = name
    .replace(/[(（][^)）]*[)）]\s*$/, '') // 末尾の丸括弧補足を除去
    .trim()
  return DASHI_INGREDIENT_NAMES.has(base)
}
