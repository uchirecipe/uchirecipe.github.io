/** ルール優先順位に沿って haystack を判定する。一致するルールが無ければ null */
function matchRules(haystack: string): IconKey | null {
  for (const rule of rules) {
    const hit = rule.words.some((word) => haystack.includes(word))
    if (!hit) continue
    const excluded = rule.exclude?.some((word) => haystack.includes(word)) ?? false
    if (excluded) continue
    return rule.key
  }
  return null
}

/** キーワードが含まれるかを判定する対象文字列をまとめる(タイトル+タグ+材料) */
function buildHaystack(input: {
  title: string
  tags: readonly string[]
  ingredients: readonly Pick<Ingredient, 'name'>[]
}): string {
  const ingredientNames = input.ingredients.slice(0, 3).map((i) => i.name)
  return [input.title, ...input.tags, ...ingredientNames].join(' ')
}

/** 料理名・タグ・先頭の材料からプレースホルダーアイコンの種別を自動選択する */
export function pickIconKey(input: {
  title: string
  tags: readonly string[]
  ingredients: readonly Pick<Ingredient, 'name'>[]
}): IconKey {
  // タイトル優先: まずタイトル単独で全ルールを試し、材料由来の誤検出(例:「だし汁」の汁で
  // スープと誤判定)がタイトルから分かる本来の種別より先に採用されるのを防ぐ
  const fromTitle = matchRules(input.title)
  if (fromTitle) return fromTitle
  const fromAll = matchRules(buildHaystack(input))
  return fromAll ?? 'default'
}

