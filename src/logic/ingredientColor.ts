/** 材料名から色トークン（CSS変数名）を選ぶ。該当なしは既定色 */
export function ingredientColorToken(name: string): string {
  const normalized = toHiragana(name)
  for (const rule of colorRules) {
    const hit = rule.words.some((word) => normalized.includes(toHiragana(word)))
    if (!hit) continue
    const excluded = rule.exclude?.some((word) => normalized.includes(toHiragana(word))) ?? false
    if (excluded) continue
    return rule.token
  }
  return '--chip-neutral'
}
