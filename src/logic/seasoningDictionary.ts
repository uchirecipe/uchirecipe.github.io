/** 一覧カードの主要食材候補から外すべき名前か（調味料・水・油・粉類・だし系・薬味少量の辞書に一致） */
export function isKnownSeasoningName(name: string): boolean {
  return SEASONING_NAMES.has(normalizeName(name))
}

/**
 * 分量に「お好みで」を含む材料は主要食材から外す（例:「お好みで」「少々(お好みで)」）。
 * 数値が混ざっていて isSeasoningLike の非数値判定をすり抜けるケース
 * （例:「1(お好みで)」）もここで拾う。
 */
export function isOptionalAmount(amount: string): boolean {
  return amount.includes('お好みで')
}
